def __get_feature_idxs():
    res= list()
    for f in __features:
        res.append(list())
        for x in range(len(f)):
            for y in range(len(f[x])):
                if f[x][y]=="X":
                    res[-1].append((x,y))
    return res



IMG_SIZE= (len(__base_feature), len(__base_feature[0]))
FEATURE_COUNT= len(__features)
FEATURE_POINTS= __get_feature_idxs()






def print_image(img_data):
    print("\n\n  ", end="")
    for x in range(len(img_data)*2+4): print("-", end="")
    print("")
    for x in range(len(img_data)):
        print("  | ", end="")
        for y in range(len(img_data[x])):
            if img_data[x][y]<0.5:
                print("  ", end="")
            else:
                print(u'\u2588\u2588', end="")
        print(" |")
    print("  ", end="")
    for x in range(len(img_data)*2+4): print("-", end="")
    print("\n")


import numpy as __np
__THRESHOLD= 0.5
def data_to_image(data, pxl_data= None):
    if pxl_data is None:
        pxl_data= __np.zeros(IMG_SIZE)
    for feature_idx, d in enumerate(data):
        if d>=__THRESHOLD:
            for x, y in FEATURE_POINTS[feature_idx]:
                pxl_data[x][y]=1
    return pxl_data